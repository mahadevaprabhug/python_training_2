# 1. What python library means
# ----------
# - python library can be a 1 or more modules like mymodule.py
# - python library can be a 1 or more package like mypackage
# - python library can be framework
#       -- framework is like mymodule.py OR mypackage
#       -- Difference
#           in mymodule.py/mypackage we are keeping only definitions
#           BUT
#           in framework, we are also implementing some
#           common functionalities or task
#           --- Example: unittest framework
#           where writing test cases are different for different projects
#           BUT executing one by one, producing total success, failres etc
#           are common. So these common tasks are implemented in the framework
#
